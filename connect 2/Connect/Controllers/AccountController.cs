        public ActionResult Image(string id)
        {
            ApplicationUser user = db.Users.Find(id);
            if (user != null && user.image != null && user.image.Length > 0)
            {
                byte[] image = user.image;
                return File(image, "image/png");
            }
            string imageFile = System.Web.HttpContext.Current.Server.MapPath("~/Content/images/avatar.png");
            var srcImage = System.Drawing.Image.FromFile(imageFile);
            var stream = new MemoryStream();
            srcImage.Save(stream, ImageFormat.Png);
            return File(stream.ToArray(), "image/png");
        }
        public ActionResult SaveProfile(string id, HttpPostedFileBase picFile)
        {
            if (picFile != null && picFile.ContentLength > 0 && picFile.ContentLength < 6 * 1000 * 100)
            {
                var user = db.Users.Find(id);
                if (user != null)
                {
                    MemoryStream target = new MemoryStream();
                    picFile.InputStream.CopyTo(target);
                    user.image = target.ToArray();
                    db.Entry(user).State = EntityState.Modified;
                    db.SaveChanges();
                }
            }
            return RedirectToAction("My");
        }
