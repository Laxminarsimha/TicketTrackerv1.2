        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public ActionResult Login(LoginModel model, string returnUrl)
        {
            if (!this.ModelState.IsValid)
            {
                return this.View(model);
            }
            bool isLdapUser = false;

            if (ValidateUserWithService(model.UserName, model.Password))
            {
                isLdapUser = true;

                ApplicationUser user = db.Users.Where(u => u.UserName.CompareTo(model.UserName) == 0).FirstOrDefault();

                //create user if not exists in db.
                if (user == null)
                {
                    user = new ApplicationUser { UserName = model.UserName, Email = user.Email };
                    var identityResult = UserManager.Create(user, model.Password);
                }

                //update password if ldap password is different from db password.
                if (user != null)
                {
                    UserManager.RemovePassword(user.Id);
                    UserManager.AddPassword(user.Id, model.Password);
                }

                //finally, login if user is not null.
                if (user != null)
                {
                    FormsAuthentication.SetAuthCookie(model.UserName, model.RememberMe);
                    var signInResult = SignInManager.PasswordSignIn(model.UserName, model.Password, model.RememberMe, shouldLockout: false);

                    using (var db = new ApplicationDbContext())
                    {
                        var usr = (from u in db.Users where u.UserName.Equals(user.UserName) select u).FirstOrDefault();
                        var userLogin = new UserLogin { ApplicationUser = usr, Ip = Request.UserHostAddress };
                        db.UserLogins.Add(userLogin);
                        db.Entry(userLogin).State = System.Data.Entity.EntityState.Added;
                        db.SaveChanges();
                    }

                    switch (signInResult)
                    {
                        case SignInStatus.Success:
                            return RedirectToLocal(returnUrl);
                        case SignInStatus.LockedOut:
                            return View("Lockout");
                        case SignInStatus.RequiresVerification:
                            return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });
                        case SignInStatus.Failure:
                        default:
                            ModelState.AddModelError(String.Empty, "Login Failed . .");
                            return View(model);
                    }
                }
            }
            this.ModelState.AddModelError(string.Empty, "Login Failed . .");
            return this.View(model);
        }

        //
        // GET: /Account/VerifyCode
        [AllowAnonymous]
        public async Task<ActionResult> VerifyCode(string provider, string returnUrl, bool rememberMe)
        {
            // Require that the user has already logged in via username/password or external login
            if (!await SignInManager.HasBeenVerifiedAsync())
            {
                return View("Error");
            }
            return View(new VerifyCodeViewModel { Provider = provider, ReturnUrl = returnUrl, RememberMe = rememberMe });
        }

        //
        // POST: /Account/VerifyCode
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> VerifyCode(VerifyCodeViewModel model)
        {
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // The following code protects for brute force attacks against the two factor codes. 
            // If a user enters incorrect codes for a specified amount of time then the user account 
            // will be locked out for a specified amount of time. 
            // You can configure the account lockout settings in IdentityConfig
            var result = await SignInManager.TwoFactorSignInAsync(model.Provider, model.Code, isPersistent: model.RememberMe, rememberBrowser: model.RememberBrowser);
            switch (result)
            {
                case SignInStatus.Success:
                    return RedirectToLocal(model.ReturnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid code.");
                    return View(model);
            }
        }

